rootElement = getRootElement()
function loadobj()
   pipetxd = engineLoadTXD("textures.txd", true)
    engineImportTXD(pipetxd,2800)
    engineImportTXD(pipetxd,2801)
    engineImportTXD(pipetxd,2802)
    engineImportTXD(pipetxd,2806)
    engineImportTXD(pipetxd,2803)
    pipe00 = engineLoadDFF("curved.dff", 0)
    engineReplaceModel(pipe00, 2800)
    pipe00c = engineLoadCOL("curved.col")
    engineReplaceCOL(pipe00c, 2800)

    pipe01 = engineLoadDFF("stright.dff", 0)
    engineReplaceModel(pipe01, 2801)
    pipe01c = engineLoadCOL("stright.col")
    engineReplaceCOL(pipe01c, 2801)

    pipe02 = engineLoadDFF("end.dff", 0)
    engineReplaceModel(pipe02, 2802)
    pipe02c = engineLoadCOL("end.col")
    engineReplaceCOL(pipe02c, 2802)

    pipe03 = engineLoadDFF("triple.dff",0)
    engineReplaceModel(pipe03, 2803)
    pipe03c = engineLoadCOL("triple.col")
    engineReplaceCOL(pipe03c, 2803)

    pipe06 = engineLoadDFF("4,5.dff", 0)
    engineReplaceModel(pipe06, 2806)
    pipe06c = engineLoadCOL("4,5.col")
    engineReplaceCOL(pipe06c, 2806)

    engineSetModelLODDistance(2800, 10000)
    engineSetModelLODDistance(2801, 10000)
    engineSetModelLODDistance(2802, 10000)
    engineSetModelLODDistance(2803, 10000)
    engineSetModelLODDistance(2806, 10000)
end
addEventHandler("onClientResourceStart", rootElement, loadobj)